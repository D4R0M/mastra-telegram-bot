function getPool() {
  if (!pool) {
    if (!process.env.DATABASE_URL) {
      if (process.env.NODE_ENV === "production") {
        console.error("\u274C DATABASE_URL is required in production");
        process.exit(1);
      } else {
        console.warn("\u26A0\uFE0F  DATABASE_URL not set - database operations will fail");
      }
    }
    pool = new Pool({
      connectionString: process.env.DATABASE_URL,
      max: 20,
      idleTimeoutMillis: 3e4,
      connectionTimeoutMillis: 2e3
    });
    pool.on("error", (err) => {
      console.error("\u{1F525} [DB] Unexpected database pool error:", err);
    });
  }
  return pool;
}

