var deleteCardTool = createTool({
  id: "delete-card-tool",
  description: `Delete a vocabulary card from the collection. This marks the card as inactive rather than permanently removing it.`,
  inputSchema: external_exports.object({
    owner_id: external_exports.coerce.number().describe("User ID who owns the card"),
    card_id: external_exports.string().describe("ID of the card to delete"),
    confirm: external_exports.boolean().default(false).describe("Confirmation that the user wants to delete the card")
  }),
  outputSchema: external_exports.object({
    success: external_exports.boolean(),
    message: external_exports.string(),
    deleted_card: external_exports.object({
      id: external_exports.string(),
      front: external_exports.string(),
      back: external_exports.string()
    }).optional()
  }),
  execute: async ({ context, mastra }) => {
    const logger = mastra?.getLogger();
    logger?.info("\u{1F527} [DeleteCard] Starting card deletion with params:", context);
    try {
      if (!context.confirm) {
        return {
          success: false,
          message: "Deletion not confirmed. Please confirm that you want to delete this card."
        };
      }
      const existingCard = await getCardById(context.card_id, context.owner_id);
      if (!existingCard) {
        return {
          success: false,
          message: "Card not found or you don't have permission to delete it."
        };
      }
      logger?.info("\u{1F4DD} [DeleteCard] Deleting card:", { id: context.card_id, front: existingCard.front, back: existingCard.back });
      const success = await deleteCard(context.card_id, context.owner_id);
      if (!success) {
        return {
          success: false,
          message: "Failed to delete card. Please try again."
        };
      }
      logger?.info("\u2705 [DeleteCard] Card deleted successfully:", { id: context.card_id });
      return {
        success: true,
        message: `Card deleted successfully: "${existingCard.front}" \u2192 "${existingCard.back}"`,
        deleted_card: {
          id: existingCard.id,
          front: existingCard.front,
          back: existingCard.back
        }
      };
    } catch (error) {
      logger?.error("\u274C [DeleteCard] Error deleting card:", error);
      return {
        success: false,
        message: `Error deleting card: ${error instanceof Error ? error.message : "Unknown error"}`
      };
    }
  }
});
