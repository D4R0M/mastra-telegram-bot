var DUPLICATE_SIMILARITY_THRESHOLD = 0.5;
function escapeHtml(value) {
  return value.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}
function formatCardSummary(card) {
  const tagsArray = Array.isArray(card.tags) ? Array.from(card.tags) : [];
  const tagsSegment = tagsArray.length > 0 ? ` <i>#${tagsArray.join(", #")}</i>` : "";
  return `- <b>${escapeHtml(card.front)}</b> -&gt; ${escapeHtml(card.back)}${tagsSegment}`;
}
function formatSimilarSection(similar, existingId) {
  const lines = similar.filter((card) => card.id !== existingId).slice(0, 5).map((card) => formatCardSummary(card));
  if (!lines.length) {
    return "";
  }
  return `Did you mean one of these?
${lines.join("\n")}`;
}
async function buildDuplicateResponse(ownerId, probe, error) {
  const existing = error.existingCard;
  const normalizedProbe = probe.trim();
  const similar = normalizedProbe ? await findSimilarCards(ownerId, normalizedProbe, { threshold: DUPLICATE_SIMILARITY_THRESHOLD }) : [];
  const similarText = formatSimilarSection(similar, existing?.id);
  let message = "This card already exists ?";
  if (existing) {
    message += `

${formatCardSummary(existing)}`;
  }
  if (similarText) {
    message += `

${similarText}`;
  }
  return {
    success: false,
    message,
    duplicate: {
      existing: existing ? {
        id: existing.id,
        front: existing.front,
        back: existing.back,
        tags: existing.tags ?? [],
        example: existing.example ?? void 0
      } : void 0,
      similar: similar.filter((card) => card.id !== existing?.id).slice(0, 5).map((card) => ({
        id: card.id,
        front: card.front,
        back: card.back,
        tags: card.tags ?? [],
        example: card.example ?? void 0,
        similarity: card.similarity
      }))
    }
  };
}
async function createCardWithDuplicateHandling(logger, payload, probe) {
  try {
    const card = await createCard(payload);
    logger?.info("[AddCard] Card created successfully:", {
      id: card.id,
      front: card.front,
      back: card.back
    });
    const tagsSegment = card.tags.length > 0 ? ` [${card.tags.join(", ")}]` : "";
    return {
      success: true,
      card_id: card.id,
      message: `Card created successfully! ${card.front} -> ${card.back}${tagsSegment}`,
      card: {
        id: card.id,
        front: card.front,
        back: card.back,
        tags: card.tags,
        example: card.example,
        lang_front: card.lang_front,
        lang_back: card.lang_back
      }
    };
  } catch (error) {
    if (error instanceof DuplicateCardError) {
      logger?.info("[AddCard] Duplicate detected:", {
        owner_id: payload.owner_id,
        front: payload.front,
        back: payload.back
      });
      return await buildDuplicateResponse(payload.owner_id, probe, error);
    }
    throw error;
  }
}
